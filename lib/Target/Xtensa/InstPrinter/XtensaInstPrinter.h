#ifndef LLVM_LIB_TARGET_XTENSA_INSTPRINTER_XTENSAINSTPRINTER_H
#define LLVM_LIB_TARGET_XTENSA_INSTPRINTER_XTENSAINSTPRINTER_H

#include "llvm/ADT/StringRef.h"
#include "llvm/MC/MCInstPrinter.h"

namespace llvm {

class XtensaInstPrinter : public MCInstPrinter {

  public:
    XtensaInstPrinter(const MCAsmInfo &MAI,
                      const MCInstrInfo &MII,
                      const MCRegisterInfo &MRI);

    // auto-generated by tblgen
    void printInstruction(const MCInst *MI, raw_ostream &O);
    static const char *getRegisterName(unsigned RegNo);

    void printRegName(raw_ostream &OS, unsigned RegNo) const override;

    void printInst(const MCInst *MI, raw_ostream &O, StringRef Annot,
                   const MCSubtargetInfo &STI) override;

  private:
    void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
    void printMemOperand(const MCInst *MI, int opNum, raw_ostream &O);
};

} // end namespace llvm

#endif /* LLVM_LIB_TARGET_XTENSA_INSTPRINTER_XTENSAINSTPRINTER_H */
